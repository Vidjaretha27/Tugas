    void pengeluaran(){
        try {
            sql = "SELECT * FROM tb_beban";
            rs = stat.executeQuery(sql);
            while(rs.next())
            {
                jComboBox1.addItem(rs.getString("nama"));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    void tampil(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            sql = "SELECT * FROM tb_beban inner join tb_pengeluaran using(id_beban) inner join tb_user using(username)";
            rs = stat.executeQuery(sql);
            while(rs.next())
            {
                Object[] o=new Object[5];
                o[0]=rs.getString("id_beban");
                o[1]=rs.getString("nama");
                o[2]=rs.getString("jumlah");
                o[3]=rs.getString("tgl");
                o[4]=rs.getString("tb_user.nama");
                model.addRow(o);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
