    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        int jumlah=Integer.parseInt(jTextField1.getText());
        String beban=jComboBox1.getSelectedItem().toString();
        Date date = new Date();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        try{
            sql="select * from tb_beban where nama='"+beban+"'";
            rs=stat.executeQuery(sql);
            rs.next();
            sql="insert into tb_pengeluaran values(NULL,'"+user+"','"+rs.getString("id_beban")+"',"
                    + "'"+jumlah+"','"+formatter.format(date)+"')";
            stat.executeUpdate(sql);
            jTextField1.setEditable(true);
            jTextField1.setText("");
            JOptionPane.showMessageDialog(null, "Data Sudah Disimpan");
            tampil();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error, "+e.getMessage());
        }
    }//GEN-LAST:event_jLabel2MouseClicked

